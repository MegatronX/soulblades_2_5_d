public enum BattleFormation
{
    /// <summary>
    /// A standard, face-to-face encounter.
    /// </summary>
    Normal,
    /// <summary>
    /// The player party gets a surprise attack on the enemy.
    /// </summary>
    PlayerAdvantage,
    /// <summary>
    /// The enemy party gets a surprise attack on the player.
    /// </summary>
    EnemyAdvantage,
    /// <summary>
    /// The player party is surrounded by one or more enemy parties.
    /// </summary>
    Pincer,
}